from app.services.llm_service import call_llm

def generate_document(task: str, k: int) -> str:
    prompt = f"""
以下のプロダクト案をもとに、Markdown形式の要件定義書を作成してください。

# 入力
{task}

# 出力条件
- 文書タイトルを先頭に書く
- 必ず以下の見出し構成に従う
- 見出しやセクションを重複させない
- 同じ内容を繰り返さない
- 曖昧表現ではなく、具体的に書く
- 主要機能は最低{k}個以上書く
- Markdown形式で出力する

# 見出し構成
# {task}要件文書

## 1. プロジェクト概要
- 目的
- 解決したい課題
- 提供価値

## 2. 主要機能
### 2.1 機能名
- 機能説明
- ユーザー価値

（同様に必要数だけ続ける）

## 3. 非機能要件
- ユーザビリティ
- パフォーマンス
- セキュリティ
- 互換性
- スケーラビリティ

## 4. 制約条件
- 予算
- 時間
- 技術上の制約

## 5. ターゲットユーザー
- ペルソナを3〜5人程度
- 年齢、職業、課題、ITリテラシーを書く

## 6. 優先順位
- 優先度順に番号付きで並べる
- MVP観点で書く

## 7. リスクと軽減策
- 技術リスク
- 運用リスク
- ユーザー利用上のリスク

# 禁止事項
- セクションの重複
- 同一文章の繰り返し
- 見出し番号の崩れ
- 未完成な文
"""
    return call_llm(prompt)