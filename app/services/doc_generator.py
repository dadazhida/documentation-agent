# app/services/doc_generator.py
from app.services.llm_service import call_llm

def generate_document(task: str, k: int) -> str:
    prompt = f"""
あなたは優秀なプロダクトマネージャーです。
以下の入力をもとに、Markdown形式の要件定義書を日本語で作成してください。

# 入力
{task}

# 出力ルール
- Markdown形式で出力する
- 文書全体は1回だけ出力する
- セクションや文章を重複させない
- 見出し番号を崩さない
- 具体的に書く
- 入力にない数値や固有条件を勝手に断定しない
- 予算・期間・対象ユーザーなど入力にない項目は「想定」「例」として記載する
- 主要機能は最低{k}個以上記載する
- タイトルは「〇〇要件文書」の形で自然な名称にする
- 「〜を開発したい」のような依頼文はタイトルに含めない

# 必須構成
# タイトル

## 1. プロジェクト概要
- 目的
- 解決したい課題
- 提供価値

## 2. 主要機能
### 2.1 機能名
- 機能説明
- ユーザー価値

## 3. 非機能要件
- ユーザビリティ
- パフォーマンス
- セキュリティ
- 互換性
- スケーラビリティ

## 4. 制約条件
- 予算
- 時間
- 技術上の制約
※ 入力に明示がない場合は「想定」と明記する

## 5. ターゲットユーザー
- 3〜5名のペルソナ例
- 年齢、職業、課題、ITリテラシーを含める
※ 入力に明示がない場合は仮説ベースで作成し、「想定ペルソナ」と明記する

## 6. 優先順位
- MVP観点で番号付きリストで記載する

## 7. リスクと軽減策
- 技術リスク
- 運用リスク
- ユーザー利用上のリスク

# 禁止事項
- 同じ見出しの重複
- 同じ文章の繰り返し
- 未完成な文
- 入力にない事実を確定情報のように書く
"""
    return call_llm(prompt)